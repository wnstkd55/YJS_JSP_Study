package polymorphism_2_3_1;

public class LgTV {
	
	public void turnOn() {
		System.out.println("LgTV -- 전원을 킨다.");
	}
	public void turnOff() {
		System.out.println("LgTV -- 전원을 끈다.");
	}
	
	public void soundUp() {
		System.out.println("LgTV -- 소리를 올린다.");
	}
	
	public void soundDown() {
		System.out.println("LgTV -- 소리를 내린다.");
	}
}
